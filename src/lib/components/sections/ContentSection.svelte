<h1>Die Adventstraße</h1>
<p class="description content">
	Die Geschichte handelt von einer tristen Straße am Rande einer Stadt. In den Häusern leben
	ver-schiedenste Menschen, die nur eint, dass sie nichts miteinander zu tun haben möchten. Jeder
	lebt für sich. Gemeinschaft und Zusammenhalt sucht man hier vergebens. Es ist der erste Dezember,
	an dem sich alles zu ändern beginnt. Ein dunkelroter Brief landet auf der Fußmatte eines
	schüchternen Klavierspielers. Er hat keinen Absender und wirkt sehr geheimnisvoll. Die Aufgabe,
	die dem Kla-vierspieler darin gestellt wird, ist nicht weniger mysteriös. An jedem der
	darauffolgenden Tage gelangt ein weiterer Brief in die Häuser der Nachbarschaft. Deren Inhalte
	sind genauso unter-schiedlich wie die Empfänger der Briefe und nicht jeder kommt seiner Aufgabe
	gerne nach. Nach und nach öffnen die Menschen ihre Herzen und obwohl keiner genau weiß, was es zu
	bedeuten hat und wer hinter all dem steht, beginnen sie zu ahnen, dass in diesem Advent etwas ganz
	Beson-deres passieren könnte. Währenddessen rückt Weihnachten immer näher und damit die Frage, was
	wohl am Heiligen Abend geschehen wird. Denn jeder der Briefe endet mit einer Einladung zum
	Weihnachtsfest in Nummer 24. Die Bewohner fragen sich, was es damit auf sich haben könnte, denn in
	ihrer Straße gibt es nur 23 Häuser… „Die Adventstraße“ besteht aus 24 Geschichten, die den Leser
	an jedem Tag in ein neues Haus mit-nehmen. Im Laufe der Zeit lernt man die gesamte Nachbarschaft
	kennen, während einem bekannte Akteure immer wieder begegnen. Zu ihnen gehören ein arbeitsloser
	Schreiner, ein trotziger cooler Teenager, eine reiche unnahbare Witwe und eine aufgedrehte
	Vierjährige. Die Geschichte soll den kleinen und großen Lesern ein Gefühl von Advent geben - von
	Ruhe und Besinnlichkeit aber auch von Spannung und Vorfreude auf das Weihnachtsfest. Die
	Geschehnisse in der Adventstraße sollen zum Nachdenken, zum Schmunzeln aber auch zum Träumen
	anregen.
</p>

