<p class="description author">
    Dorothea Schaller wurde 1994 in Illertissen geboren. Nach ihrem Abitur und einer Ausbildung im Bereich
    Tourismusmanagement arbeitete sie für einige Jahre in der Gastronomie und Eventbranche in der Schweiz. Dort
    entdeckte sie ihre Liebe zum Schreiben. Neben dem Lesen spannender Bücher und dem Organisieren spektakulärer
    Events liebt Dorothea Schal-ler es zu reisen. Während einer Rundreise durch Neuseeland und Indo-nesien,
    entstanden ihre ersten Kinderbuchillustrationen. Die Idee für ihr Buch „die Adventstraße“ kam Dorothea
    Schaller
    bei einer Zugfahrt ent-lang des Zürichsees.
    Schon seit Kindertagen ist für sie die Vorweihnachtszeit eine der schöns-ten Zeiten im Jahr. Der erste
    Schnee,
    die vielen Lichter und die alten Ri-tuale, wie das Plätzchen backen und Wohnung schmücken, machen für sie
    den
    Advent zu etwas ganz Besonderem. Mit ihrem Buch schaffte sie es, eine neue Tradition in ihrer Familie zu
    etablieren. An jedem Abend im Advent werden nun die Lichter gedimmt, Plätzchen bereitgestellt und der
    Adventskranz entzündet. Dann setzen sich alle zusammen und lauschen einer neuen Adventsgeschichte
</p>

